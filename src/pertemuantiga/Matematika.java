package pertemuantiga;

public class Matematika {
    
    int nilai1;
    int nilai2;
    
    int tambah(){
        int a = nilai1 + nilai2;
        return a;
    }
    int kali(){
        int hasil = nilai1 * nilai2;
        return hasil;
    }
    int kurang(){
        int hasil = nilai1 - nilai2;
        return hasil;
    }
    int bagi(){
        int hasil = nilai1 / nilai2;
        return hasil;
    }
}