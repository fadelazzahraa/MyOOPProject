package pertemuandua;

public class PercabanganSwitchCase {

public static void main(String[] args) {
        int angka = 3;
        switch (angka) {
            case 1: 
                System.out.println("Satu");
                break;
            case 2: 
                System.out.println("Dua");
                break;
            case 3: 
                System.out.println("Tiga");
                break;
            default:
                System.out.println("Selain Satu sampai Tiga");
                break;
        }
    }    
}
