package pertemuandua;

public class OperatorUnary {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        int c = a + b;
        System.out.println(++c);
        System.out.println(c++);
        System.out.println(++c);
        System.out.println(--c);
        System.out.println(c--);
        System.out.println(--c);
    }    
}
