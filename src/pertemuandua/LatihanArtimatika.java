
package pertemuandua;

public class LatihanArtimatika {
    public static void main(String args[]){
        double a, b;
        double hasilTambah, hasilKurang, hasilKali;
        double hasilMod, hasilBagi;
        a = 25;
        b = 10;
        
        hasilTambah = a + b;
        hasilKurang = a - b;
        hasilKali = a * b;
        hasilBagi = a / b;
        hasilMod = a % b;
        
        System.out.println(hasilTambah);
        System.out.println(hasilKurang);
        System.out.println(hasilKali);
        System.out.println(hasilBagi);
        System.out.println(hasilMod);
    }
}
