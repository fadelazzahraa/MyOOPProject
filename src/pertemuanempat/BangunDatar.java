package pertemuanempat;

public class BangunDatar {
    int hitungLuas(int panjang, int lebar){
        return panjang * lebar;
    }
    
    int hitungLuas(int sisi){
        return sisi * sisi;
    }
    
    double hitungLuas(double alas, double tinggi){
        return alas * tinggi / 2;
    }
}
